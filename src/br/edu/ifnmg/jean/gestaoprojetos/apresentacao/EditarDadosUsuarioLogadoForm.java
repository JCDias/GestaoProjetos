    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        Usuario user = new Usuario();
        String nome = txtNome.getText().trim();
        String email = txtEmail.getText().trim();
        String senha = txtSenha.getText().trim();
        String confirmaSenha = txtConfirmSenha.getText().trim();
        
        UsuarioBO usuarioBO = new UsuarioBO();
        
        String valida = usuarioBO.validaDadosSalvar(nome, email);

        //Setar usuario
        user.setNome(nome);
        user.setEmail(email);
        user.setId_usuario(userlogado.getId_usuario());
        System.out.println("senha "+senha + " cONFIRMA "+confirmaSenha);
        
        if (!senha.equals("") && !confirmaSenha.equals("")) {
            if (senha.equals(confirmaSenha)) {
                Criptografia criptografia = new Criptografia();
                String senhaCriptografada = criptografia.criptografar(senha);
                user.setSenha(senhaCriptografada);
                valida = null;
            } else {
                valida = "As senhas não conferem!";
            }
        } else {
            valida = "Preencha todos os campos!";
        }
        
        if (valida == null) {
            try {
                
                usuarioBO.atualizaPerfil(user);
                JOptionPane.showMessageDialog(null, "Dados Atualizados com Sucesso", "Atualizar perfil", JOptionPane.INFORMATION_MESSAGE);
                logger.info("Dados do perfil Atualizados com Sucesso");
                this.dispose();
            } catch (SQLException ex) {
                logger.error("Erro ao atualizar dados do perfil "+ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, valida, "Atualizar perfil", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

